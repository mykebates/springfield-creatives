<h1>Frontend Dev</h1>
<p>You should mostly stick to CSS changes when possible.</p>
<p>To edit CSS, open a terminal and run <b>compass watch</b> inside the "wp-content/themes/wp-theme" directory. You will need to have compass installed first. Instructions to do that are <a href="">here</a>.</p>
